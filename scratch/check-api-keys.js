const { PrismaClient } = require('@prisma/client');
require('dotenv').config();

const prisma = new PrismaClient();

async function main() {
  console.log('🔍 Checking site configuration and database health...');
  try {
    const apiKeys = await prisma.apiKey.findMany();
    console.log('\n--- API KEYS ---');
    if (apiKeys.length === 0) {
      console.log('⚠️ No API keys found in database.');
    } else {
      apiKeys.forEach(k => {
        console.log(`- Provider: ${k.provider}, Model: ${k.model || 'N/A'}, Active: ${k.isActive}, Key (masked): ${k.apiKey ? k.apiKey.slice(0, 8) + '...' : 'None'}`);
      });
    }

    const settings = await prisma.autoBlogSettings.findUnique({ where: { id: 'default' } });
    console.log('\n--- AUTO-BLOG SETTINGS ---');
    if (!settings) {
      console.log('⚠️ No Auto-Blog settings found.');
    } else {
      console.log(`- Active: ${settings.isActive}`);
      console.log(`- Frequency: ${settings.frequency}`);
      console.log(`- Max posts per run: ${settings.maxPostsPerRun}`);
      console.log(`- Max posts per day: ${settings.maxPostsPerDay}`);
      console.log(`- Image Source: ${settings.imageSource}`);
      console.log(`- Auto Publish: ${settings.autoPublish}`);
    }

    const pendingCount = await prisma.autoBlogKeyword.count({ where: { status: 'pending' } });
    const usedCount = await prisma.autoBlogKeyword.count({ where: { status: 'used' } });
    console.log('\n--- AUTO-BLOG KEYWORDS STATS ---');
    console.log(`- Pending: ${pendingCount}`);
    console.log(`- Used: ${usedCount}`);

    const latestPosts = await prisma.blogPost.findMany({
      orderBy: { createdAt: 'desc' },
      take: 5,
      select: { title: true, status: true, autoGenerated: true, createdAt: true }
    });
    console.log('\n--- LATEST POSTS ---');
    if (latestPosts.length === 0) {
      console.log('ℹ️ No blog posts in database.');
    } else {
      latestPosts.forEach(p => {
        console.log(`- Title: "${p.title}", Status: ${p.status}, AutoGen: ${p.autoGenerated}, Created: ${p.createdAt}`);
      });
    }

  } catch (error) {
    console.error('❌ Error reading database:', error);
  } finally {
    await prisma.$disconnect();
  }
}

main();
