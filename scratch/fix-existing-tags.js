const { PrismaClient } = require('@prisma/client');
require('dotenv').config();

const prisma = new PrismaClient();

async function main() {
  console.log('🔄 Attaching grid tags to existing blog posts...');
  try {
    const posts = await prisma.blogPost.findMany({
      where: { autoGenerated: true }
    });

    console.log(`Found ${posts.length} auto-generated posts.`);

    for (const post of posts) {
      console.log(`Post: "${post.title}"`);
      const lowerTitle = post.title.toLowerCase();

      const tagsToAdd = [];
      if (lowerTitle.includes('recruitment') || lowerTitle.includes('bharti') || lowerTitle.includes('vacancy') || lowerTitle.includes('भर्ती') || lowerTitle.includes('नौकरी')) {
        tagsToAdd.push('Job', 'Vacancy', 'Career');
      }
      
      if (tagsToAdd.length > 0) {
        console.log(`- Adding tags: ${tagsToAdd.join(', ')}`);
        
        for (const tagName of tagsToAdd) {
          const tag = await prisma.tag.upsert({
            where: { name: tagName },
            update: {},
            create: {
              name: tagName,
              slug: tagName.toLowerCase().replace(/[^a-z0-9]+/g, '-')
            }
          });

          try {
            await prisma.postTag.upsert({
              where: {
                postId_tagId: {
                  postId: post.id,
                  tagId: tag.id
                }
              },
              update: {},
              create: {
                postId: post.id,
                tagId: tag.id
              }
            });
          } catch(e) {
            console.log(`  Tag "${tagName}" already linked or error:`, e.message);
          }
        }
      }
    }
    console.log('✅ Tag updates finished successfully.');
  } catch (error) {
    console.error('❌ Error updating tags:', error);
  } finally {
    await prisma.$disconnect();
  }
}

main();
